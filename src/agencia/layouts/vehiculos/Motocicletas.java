    private void btnAnadirMotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnadirMotoActionPerformed
        // TODO add your handling code here:
        if (txtID.getText().isEmpty() || txtMarca.getText().isEmpty() || txtModelo.getText().isEmpty() || txtAnio.getText().isEmpty()
                || txtColor.getText().isEmpty() || txtPrecio.getText().isEmpty() || txtEstado.getText().isEmpty() || txtCilindrada.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No dejes Campos Vacios");
        } else {
            String ID = txtID.getText().trim();
            String Marca = txtMarca.getText().trim();
            String Modelo = txtModelo.getText().trim();
            String Anio = txtAnio.getText().trim();
            String Color = txtColor.getText().trim();
            String Precio = txtPrecio.getText().trim();
            String Estado = txtEstado.getText().trim();
            String Cilindrada = txtCilindrada.getText().trim();
            ///metodos traidos de la clase Autos Metodo set
            Motos NuevaMoto = new Motos(ID, Marca, Modelo, Anio, Color, Precio, Estado, Cilindrada);
            // guardar registros en la tabla y archivo.txt
            metodos.Guardar(NuevaMoto);
            metodos.GuardarArchivos(NuevaMoto);
            tblMotos.setModel(metodos.ListaMotos());

            JOptionPane.showMessageDialog(null, "Moto Registrada");
            //limpiar cajas de texto
            txtID.setText("");
            txtMarca.setText("");
            txtModelo.setText("");
            txtAnio.setText("");
            txtColor.setText("");
            txtPrecio.setText("");
            txtEstado.setText("");
            txtCilindrada.setText("");
        }
    }//GEN-LAST:event_btnAnadirMotoActionPerformed

