    private void btnEliminarMotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMotoActionPerformed
        // TODO add your handling code here:
        this.tblMotosTXT = (DefaultTableModel) tblMotos.getModel();
        int filaSeleccionada = tblMotos.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Debes Seleccionar un registro para eliminar");
            return;
        }
        
        int respuesta = JOptionPane.showConfirmDialog(this, "Estas Seguro de Eliminar este registro?");
        if (respuesta == JOptionPane.YES_OPTION) {
            tblMotosTXT.removeRow(tblMotos.getSelectedRow());
            try {
                String archivo = "Motos.txt";
                BufferedWriter almacenado = new BufferedWriter(new FileWriter(archivo));
                for (int i = 0; i < tblMotos.getRowCount(); i++) {
                    for (int j = 0; j < tblMotos.getColumnCount(); j++) {
                        almacenado.write((String)(tblMotos.getValueAt(i, j)));
                        if(j < tblMotos.getColumnCount() -1){
                            almacenado.write("|");
                        }
                    }
                    almacenado.newLine();
                }
                almacenado.close();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            JOptionPane.showMessageDialog(null, "Auto Eliminado correctamente.");
            limpiarCampos();

        } else {
            limpiarCampos();
        }
    }//GEN-LAST:event_btnEliminarMotoActionPerformed

    private void btnModificarMotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarMotoActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tblMotos.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Error: Debes seleccionar una fila para modificar.");
            return; // Detiene la ejecución si no hay nada seleccionado
        }
        String datos[] = new String[8];
        datos[0] = txtID.getText().trim();
        datos[1] = txtMarca.getText().trim();
        datos[2] = txtModelo.getText().trim();
        datos[3] = txtAnio.getText().trim();
        datos[4] = txtColor.getText().trim();
        datos[5] = txtPrecio.getText().trim();
        datos[6] = txtEstado.getText().trim();
        datos[7] = txtCilindrada.getText().trim();

        for (int columna = 0; columna < tblMotos.getColumnCount(); columna++) {
            tblMotos.setValueAt(datos[columna], filaSeleccionada, columna);
        }//cierra for para  iterar en la columnas a modficar

        try {
            String archivo = "Motos.txt";
            BufferedWriter almacenado = new BufferedWriter(new FileWriter(archivo));
            for (int i = 0; i < tblMotos.getRowCount(); i++) {
                for (int j = 0; j < tblMotos.getColumnCount(); j++) {
                    almacenado.write((String) (tblMotos.getValueAt(i, j)));
                    if (j < tblMotos.getColumnCount() - 1) {
                        almacenado.write("|");
                    }
                }
                almacenado.newLine();
            }
            almacenado.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }// cierra Try
        JOptionPane.showMessageDialog(null, "Moto Actualizada");
        limpiarCampos();

    }//GEN-LAST:event_btnModificarMotoActionPerformed
